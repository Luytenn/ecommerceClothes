package beans;

public class Administrador {
	
	private int id_admin;
	private String nombreAdmin, apepatAdmin, apematAdmin, usuario_admin, contraseña_admin;
	
	
	public int getId_admin() {
		return id_admin;
	}
	public void setId_admin(int id_admin) {
		this.id_admin = id_admin;
	}
	public String getNombreAdmin() {
		return nombreAdmin;
	}
	public void setNombreAdmin(String nombreAdmin) {
		this.nombreAdmin = nombreAdmin;
	}
	public String getApepatAdmin() {
		return apepatAdmin;
	}
	public void setApepatAdmin(String apepatAdmin) {
		this.apepatAdmin = apepatAdmin;
	}
	public String getApematAdmin() {
		return apematAdmin;
	}
	public void setApematAdmin(String apematAdmin) {
		this.apematAdmin = apematAdmin;
	}
	public String getUsuario_admin() {
		return usuario_admin;
	}
	public void setUsuario_admin(String usuario_admin) {
		this.usuario_admin = usuario_admin;
	}
	public String getContraseña_admin() {
		return contraseña_admin;
	}
	public void setContraseña_admin(String contraseña_admin) {
		this.contraseña_admin = contraseña_admin;
	}
	
	
	
	

}
